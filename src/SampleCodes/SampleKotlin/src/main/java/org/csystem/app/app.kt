    ve aşağıdaki kod ile test ediniz.
    Algoritma: 123 -> 3 -> 3 * 10 + 2 = 32 -> 32 * 10 + 1 = 321
----------------------------------------------------------------------------------------------------------------------*/
package org.csystem.app

fun main()
{
    for (value in 10 downTo 1)
        println(value)
}

